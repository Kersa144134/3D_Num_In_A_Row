using System;

namespace OptionSystem.Domain
{
    /// <summary>
    /// ゲーム成立条件を定義するデータモデル
    /// </summary>
    [Serializable]
    public class GameRules
    {
        // ======================================================
        // 列挙体
        // ======================================================

        /// <summary>盤面サイズ種別</summary>
        public enum BoardSizeType
        {
            Size3 = 3,
            Size5 = 5
        }
        
        // ======================================================
        // プロパティ
        // ======================================================

        // --------------------------------------------------
        // プレイヤー
        // --------------------------------------------------
        /// <summary>プレイヤー人数</summary>
        public int PlayerCount;

        // --------------------------------------------------
        // ゲーム進行
        // --------------------------------------------------
        /// <summary>総ターン数</summary>
        public int TurnCount;

        /// <summary>1 プレイヤーあたりの制限時間（秒）</summary>
        public float PerPlayerLimitTime;

        // --------------------------------------------------
        // ボード
        // --------------------------------------------------
        /// <summary>盤面サイズ</summary>
        public BoardSizeType BoardSize;

        /// <summary>勝利条件（連続数）</summary>
        public int ConnectCount;

        // --------------------------------------------------
        // 操作
        // --------------------------------------------------
        /// <summary>カメラ回転速度（度/秒）</summary>
        public float CameraRotationSpeed;

        /// <summary>ポインター移動速度</summary>
        public float PointerSpeed;
    }
}